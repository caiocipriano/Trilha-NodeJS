let numero=2;

console.log(numero + numero);
console.log(numero - numero);
console.log(numero * numero);
console.log(numero / numero);
console.log(numero ** numero);

//Iteração
console.log(numero++);

//Atribuição 
console.log(numero += numero);
console.log(numero -= numero);

//Igualdade escrita
console.log(1 ===1); //Valor e Tipo = true

console.log('1' === 1); //False

//Igualdade Solta
console.log(1==1); //Valor = True


